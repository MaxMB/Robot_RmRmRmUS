function [dJqB] = mtx_dJqB (q4, q5, dq4, dq5, lB)
dJqB = [lB.*(dq4.*cos(q5).*sin(q4)+dq5.*cos(q4).*sin(q5)), ...
  lB.*(dq5.*cos(q5).*sin(q4)+dq4.*cos(q4).*sin(q5));
  lB.*((-1).*dq4.*cos(q4).*cos(q5)+dq5.*sin(q4).*sin(q5)), ...
  lB.*((-1).*dq5.*cos(q4).*cos(q5)+dq4.*sin(q4).*sin(q5));
  0, ...
  -dq5.*lB.*sin(q5)];